type JoinWithDot<T extends (string | symbol)[]> = T extends []
  ? never
  : T extends [infer F]
  ? F
  : T extends [infer F, ...infer R]
  ? F extends string | symbol
    ? R extends (string | symbol)[]
      ? // @ts-ignore
        `${F}.${JoinWithDot<R>}`
      : never
    : never
  : never;

// Final translation key type that includes all possible paths
export type TranslationKey = JoinWithDot<PathsToStringProps<typeof LOCALES.en>>;

// Helper function to get nested value
function getNestedValue(obj: NestedTranslations, path: string): string {
  return path.split(".").reduce((acc, part) => {
    if (acc && typeof acc === "object" && part in acc) {
      return acc[part];
    }
    return undefined;
  }, obj as any) as string;
}

// Updated translation function
export function getTranslation(locale: Locale, key: TranslationKey): string {
  const localeTranslation = getNestedValue(LOCALES[locale], key);
  if (localeTranslation) return localeTranslation;

  const fallbackTranslation = getNestedValue(LOCALES.en, key);
  if (fallbackTranslation) return fallbackTranslation;

  return key;
}
